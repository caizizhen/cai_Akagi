let _current = "en";
const _listeners = new Set();

export function setLang(code) {
  if (!LANGS[code]) return;
  _current = code;
  document.documentElement.lang = LANGS[code].htmlLang;
  applyDom();
  for (const cb of _listeners) cb(code);
  try { localStorage.setItem("akagi.lang", code); } catch {}
}

export function getLang() { return _current; }
export function getPack() { return LANGS[_current]; }
export function onLangChange(cb) { _listeners.add(cb); return () => _listeners.delete(cb); }

export function t(key, vars) {
  const dict = LANGS[_current].dict;
  let s = dict[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, v);
  return s;
}

export function applyDom(root = document) {
  for (const el of root.querySelectorAll("[data-i18n]")) {
    const key = el.getAttribute("data-i18n");
    el.textContent = t(key);
  }
}

export function initFromStorage() {
  let saved = null;
  try { saved = localStorage.getItem("akagi.lang"); } catch {}
  const detected = saved || (navigator.language?.startsWith("zh-TW") ? "zh-TW"
    : navigator.language?.startsWith("zh") ? "zh-CN"
    : navigator.language?.startsWith("ja") ? "ja"
    : "en");
  setLang(LANGS[detected] ? detected : "en");
}
